<?php

    $a = 5;
    $b = 3;

    // Igualdade

    if($a == $b){
        echo "A é igual a B <br>";
    }

    // Diferença 
    if($a != $b){
        echo "A é diferente a B  <br>";    
    }

    // Idêntico 
    if($a === $b){
        echo "A é idêntico a B <br>";    
    }

    // Não indêntico 
    if($a !== $b){
        echo "A não é idêntico a B <br>";    
    }